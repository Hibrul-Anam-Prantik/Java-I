package TypeCasting;

public class Casting
{
    public static void main(String[] args) {
//        Primitive data types
        // implicit typeCasting
        int x = 7;
        double y = x;
        System.out.println(y);
        // explicit typeCasting
        double d = 17.2;
        int n = (int) d;
        System.out.println(n);
    }
}
